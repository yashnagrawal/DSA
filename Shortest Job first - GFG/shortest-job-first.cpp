class Solution {
  public:
    long long solve(vector<int>& bt) {
        // code here
        sort(bt.begin(), bt.end());
        
        long long n = bt.size();
        
        long long total_time = 0;
        long long curr_time = 0;
        
        for(auto time: bt){
            total_time+=curr_time;
            curr_time+=time;
        }
        
        return total_time/n;
    }
};

